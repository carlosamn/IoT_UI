  /**
   *  --------------------
   * |  Method Name : initSensorStore()
   * |  Description : /GET pull and fill an Store with all QSO Sensors data.
   * |  @param : none
   * |  @returns : Promise resolving array of QSOSensor's objects.
   *  --------------------
   */
  initSensorStore() {
    return new Promise(async (resolve, reject) => {

      const currentProjectId = JSON.parse(localStorage.getItem('currentLocation')).projectId;

      const filterByProject = filterQuery({ projectId: currentProjectId });
      const urlLocations = `${this.path.base}/${this.path.model}${filterByProject}`;

      this.http
        .get<any[]>(urlLocations)
        .toPromise()
        .then(sensors => {
          const qsoSensors: ISensorQSO[] = sensors.map(sensor => toISensorQSO(sensor));

          const currentUids = sensors.map(sensor => sensor.uid);
          const filterByUids = filterQueryUIDs({ field: 'uid', values: currentUids });

          const urlAnalog = `${this.path.base}/ainputs${filterByUids}`;
          const urlDevices = `${this.path.base}/devices/sensordata/uid${filterByUids}`;

          console.warn('urlDevices');
          console.warn(urlDevices)

          //this.http.get<any[]>(urlDevices).toPromise().then(devices => console.warn(devices) );

          this.http.get<any[]>(urlAnalog).toPromise().then(analog => {
              qsoSensors.map(qsoSensor => {
                const analogByuid = analog.filter(analogItem => analogItem.uid === qsoSensor.uid);

                for (let k = 0; k < analogByuid.length; k++) {
                  if (analogByuid[k].enable) {
                    qsoSensor.channels[k].id = k;
                    qsoSensor.channels[k].name = analogByuid[k].name;
                    qsoSensor.channels[k].units = analogByuid[k].unit;
                    qsoSensor.channels[k].alarms = {
                      high: {
                        enable: analogByuid[k].enable_alarm_h,
                        limit: analogByuid[k].alarm_h
                      },
                      low: {
                        enable: analogByuid[k].enable_alarm_l,
                        limit: analogByuid[k].alarm_l
                      }
                    };
                  }
                }

                return qsoSensor;
              });

              this.postSensorStore(qsoSensors);
              resolve({
                status: 'OK',
                type: 'Sucessfully Promise',
                parent: 'qso.service.ts',
                data: qsoSensors
              });

            }).catch(error => {
              resolve({
                status: 'ERROR',
                type: 'Error on Promise',
                parent: 'qso.service.ts',
                description: 'HTTP GET ERROR PULLING',
                url: urlAnalog
              });
            });
        }).catch(error => {
          resolve({
            status: 'ERROR',
            type: 'Error on Promise',
            parent: 'qso.service.ts',
            description: 'HTTP GET ERROR PULLING',
            url: urlLocations
          });
        });
    });
  }



